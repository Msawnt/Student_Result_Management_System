    private void studentDetailsSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_studentDetailsSaveActionPerformed
        // TODO add your handling code here:
        String rollno = rollNumber.getText();
        String course =(String) courseOption.getSelectedItem();
        String branchName = (String) branchNameOption.getSelectedItem();
        String name = Name.getText();
        String gender = (String) Gender.getSelectedItem();
        String fathername = fName.getText();
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/resultManagement","root","mayuS@123");
            
            Statement st = con.createStatement();
            st.executeUpdate("insert into student(rollno,course,branchName,name,gender,fathername) values('"+rollno+"','"+course+"','"+branchName+"','"+name+"','"+gender+"','"+fathername+"')");
            JOptionPane.showMessageDialog(null, "Data added Successfully!");
            setVisible(false);
            new adminHome().setVisible(true);
        } catch(Exception e){
             JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_studentDetailsSaveActionPerformed

